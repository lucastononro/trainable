"""Trainable CLI — setup wizard and launcher."""

from __future__ import annotations

import getpass
import os
import shutil
import subprocess
import sys
import textwrap
import urllib.request
from pathlib import Path

REPO = "lucastononro/trainable"
BRANCH = "main"
RAW_BASE = f"https://raw.githubusercontent.com/{REPO}/{BRANCH}"

COMPOSE_FILE = "docker-compose.prod.yml"
ENV_FILE = ".env"

BOLD = "\033[1m"
DIM = "\033[2m"
GREEN = "\033[32m"
YELLOW = "\033[33m"
CYAN = "\033[36m"
RED = "\033[31m"
RESET = "\033[0m"


def banner():
    print(
        textwrap.dedent(f"""\

        {BOLD}╔══════════════════════════════════════╗
        ║          🚀  trainable  🚀           ║
        ║   AI-powered ML experimentation      ║
        ╚══════════════════════════════════════╝{RESET}
    """)
    )


def step(n: int, msg: str):
    print(f"\n{CYAN}{BOLD}[{n}/4]{RESET} {BOLD}{msg}{RESET}")


def success(msg: str):
    print(f"  {GREEN}✓{RESET} {msg}")


def warn(msg: str):
    print(f"  {YELLOW}!{RESET} {msg}")


def fail(msg: str):
    print(f"  {RED}✗{RESET} {msg}")


def prompt_secret(label: str, help_url: str | None = None) -> str:
    hint = f" {DIM}({help_url}){RESET}" if help_url else ""
    while True:
        value = getpass.getpass(f"  {label}{hint}: ").strip()
        if value:
            return value
        warn("Cannot be empty, try again.")


def prompt_optional(label: str, default: str) -> str:
    value = input(f"  {label} {DIM}[{default}]{RESET}: ").strip()
    return value or default


def download_file(filename: str, dest: Path):
    url = f"{RAW_BASE}/{filename}"
    urllib.request.urlretrieve(url, dest / filename)
    success(f"Downloaded {filename}")


def check_docker():
    if not shutil.which("docker"):
        fail("Docker not found. Install it from https://docs.docker.com/get-docker/")
        sys.exit(1)
    result = subprocess.run(
        ["docker", "compose", "version"],
        capture_output=True,
        text=True,
    )
    if result.returncode != 0:
        fail("Docker Compose not found. Install Docker Desktop or the compose plugin.")
        sys.exit(1)
    success("Docker and Docker Compose found")


def write_env(dest: Path, config: dict[str, str]):
    lines = [
        "# Generated by: trainable init",
        "",
        "# === Required ===",
        f"ANTHROPIC_API_KEY={config['ANTHROPIC_API_KEY']}",
        f"MODAL_TOKEN_ID={config['MODAL_TOKEN_ID']}",
        f"MODAL_TOKEN_SECRET={config['MODAL_TOKEN_SECRET']}",
        "",
        "# === Optional ===",
        f"CLAUDE_MODEL={config.get('CLAUDE_MODEL', 'claude-sonnet-4-20250514')}",
    ]
    env_path = dest / ENV_FILE
    env_path.write_text("\n".join(lines) + "\n")
    success(f"Wrote {ENV_FILE}")


def cmd_init():
    banner()

    dest = Path.cwd()

    # Step 1 — check Docker
    step(1, "Checking prerequisites")
    check_docker()

    # Step 2 — download compose file
    step(2, "Downloading docker-compose.prod.yml")
    if (dest / COMPOSE_FILE).exists():
        warn(f"{COMPOSE_FILE} already exists, overwriting")
    download_file(COMPOSE_FILE, dest)

    # Step 3 — wizard
    step(3, "Configuring secrets")
    print()

    config = {}
    config["ANTHROPIC_API_KEY"] = prompt_secret(
        "Anthropic API key",
        "https://console.anthropic.com",
    )
    config["MODAL_TOKEN_ID"] = prompt_secret(
        "Modal Token ID",
        "https://modal.com/settings",
    )
    config["MODAL_TOKEN_SECRET"] = prompt_secret("Modal Token Secret")
    print()
    config["CLAUDE_MODEL"] = prompt_optional("Claude model", "claude-sonnet-4-20250514")

    if (dest / ENV_FILE).exists():
        warn(f"{ENV_FILE} already exists, overwriting")
    write_env(dest, config)

    # Step 4 — launch
    step(4, "Ready to launch")
    print()
    answer = input(f"  Start trainable now? {DIM}[Y/n]{RESET}: ").strip().lower()
    if answer in ("", "y", "yes"):
        cmd_up()
    else:
        print(
            f"\n  Run {BOLD}trainable up{RESET} when you're ready, "
            f"or: {DIM}docker compose -f {COMPOSE_FILE} up{RESET}\n"
        )


def cmd_up():
    compose = Path.cwd() / COMPOSE_FILE
    if not compose.exists():
        fail(f"{COMPOSE_FILE} not found. Run {BOLD}trainable init{RESET} first.")
        sys.exit(1)
    if not (Path.cwd() / ENV_FILE).exists():
        fail(f"{ENV_FILE} not found. Run {BOLD}trainable init{RESET} first.")
        sys.exit(1)

    print(f"\n{GREEN}{BOLD}Starting trainable...{RESET}")
    print(f"{DIM}  Frontend:      http://localhost:3000")
    print(f"  Backend API:   http://localhost:8000")
    print(f"  MinIO Console: http://localhost:9001{RESET}\n")

    os.execvp(
        "docker",
        ["docker", "compose", "-f", COMPOSE_FILE, "up"],
    )


def cmd_down():
    compose = Path.cwd() / COMPOSE_FILE
    if not compose.exists():
        fail(f"{COMPOSE_FILE} not found.")
        sys.exit(1)
    os.execvp(
        "docker",
        ["docker", "compose", "-f", COMPOSE_FILE, "down"],
    )


USAGE = f"""\
{BOLD}trainable{RESET} — AI-powered ML experimentation platform

{BOLD}Usage:{RESET}
  trainable init    Set up trainable in the current directory (wizard)
  trainable up      Start all services
  trainable down    Stop all services

{BOLD}Quick start:{RESET}
  pip install trainable
  mkdir trainable && cd trainable
  trainable init
"""


def main():
    args = sys.argv[1:]
    cmd = args[0] if args else None

    if cmd == "init":
        cmd_init()
    elif cmd == "up":
        cmd_up()
    elif cmd == "down":
        cmd_down()
    else:
        print(USAGE)
        sys.exit(0 if cmd in ("-h", "--help") else 1)


if __name__ == "__main__":
    main()
